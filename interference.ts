// person-interface-example.ts

// Define an interface called Person
interface Person {
  name: string;
  age: number;
  email: string;
}

// Declare a variable user of type Person and initialize it with sample data
const user: Person = {
  name: 'Vishal Reddy',
  age: 20,
  email: 'Vishalreddy1501@gmail.com'
};

// Example usage
console.log('User:', user);
